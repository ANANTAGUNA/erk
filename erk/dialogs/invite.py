from PyQt5.QtWidgets import *
from PyQt5.QtGui import *
from PyQt5.QtCore import *
from PyQt5 import QtCore

from erk.common import *

class Dialog(QDialog):

	@staticmethod
	def get_invite_information(parent=None):
		dialog = Dialog(parent)
		r = dialog.exec_()
		if r:
			return dialog.return_info()
		return None

		self.close()

	def return_info(self):

		return self.invite_channel

	def setChannel(self):
		self.invite_channel = self.channels.itemText(self.channels.currentIndex())

	def __init__(self,parent=None):
		super(Dialog,self).__init__(parent)

		self.parent = parent

		self.setWindowTitle(f"Invite")
		self.setWindowIcon(QIcon(CHANNEL_WINDOW))

		self.channels = QComboBox(self)
		self.channels.activated.connect(self.setChannel)

		clist = self.parent.gui.getChannelList(self.parent.client)
		if len(clist)>0:
			for c in clist:
				self.channels.addItem(c)
			self.invite_channel = self.channels.itemText(0)

		# Buttons
		buttons = QDialogButtonBox(self)
		buttons.setStandardButtons(QDialogButtonBox.Cancel|QDialogButtonBox.Ok)
		buttons.accepted.connect(self.accept)
		buttons.rejected.connect(self.reject)

		desc = QLabel("Invite <b><i>"+self.parent.name+"</b></i> to...")
		desc.setAlignment(Qt.AlignCenter)

		finalLayout = QVBoxLayout()
		finalLayout.addWidget( desc )
		finalLayout.addWidget(self.channels)
		finalLayout.addStretch()
		finalLayout.addWidget(buttons)

		self.setWindowFlags(self.windowFlags()
                    ^ QtCore.Qt.WindowContextHelpButtonHint)

		self.setLayout(finalLayout)
